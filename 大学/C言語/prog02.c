#include <stdio.h>

int main(void)
{
    int value;

    value = 10 + 3;
    printf("valueの値は%dです\n", value);
    value = 10 - 3;
    printf("valueの値は%dです\n", value);
    value = 10 * 3;
    printf("valueの値は%dです\n", value);
    value = 10 / 3;
    printf("valueの値は%dです\n", value);
    value = 10 % 3;
    printf("valueの値は%dです\n", value);

    return 0;
}
